public class Wrapper_class_Example {
    public static void main(String[]args){

        // Concerting to the Wrapper
        Integer obj=new Integer(3);

        // Convert from wrapper to simple by Explicity
        int MyObject=obj.intValue();


        System.out.println("\n\nFrom the Wrapper Class the value is "+obj);

        System.out.println("From simple variable  is "+MyObject);
    }
}
